	// create n colors that are as separated on a HSB color wheel as possible
	
	public static Color[] getHSBWheelScheme(int n){

		Color[] scheme=new Color[n];

		// if 12 colors or less, vary hue and use 100% saturation and brightness
		if(n<=12){
			for(int i=0;i<n;i++){
				scheme[i]=Color.getHSBColor((float)i/n,1.0f,1.0f);
			}
		}
		// if 24 colors or less, use a two-tier saturation (50% and 100%)
		else if(n<=24){
			int t=n/2;
			for(int i=0;i<t;i++){
				scheme[i]=Color.getHSBColor((float)i/t,1.0f,1.0f);
			}
			for(int i=0;i<n-t;i++){
				scheme[i+t]=Color.getHSBColor((float)i/(n-t),0.5f,1.0f);
			}
		}
		// if more than 24 colors, use a two-tier saturation and a two-tier brightness (50% and 100%)
		else{	
			int t=n/4;
			for(int i=0;i<t;i++){
				scheme[i]=Color.getHSBColor((float)i/t,1.0f,1.0f);
			}
			for(int i=0;i<t;i++){
				scheme[i+t]=Color.getHSBColor((float)i/t,0.5f,1.0f);
			}
			for(int i=0;i<t;i++){
				scheme[i+(2*t)]=Color.getHSBColor((float)i/t,1.0f,0.5f);
			}
			for(int i=0;i<n-(3*t);i++){
				scheme[i+(3*t)]=Color.getHSBColor((float)i/t,0.5f,0.5f);
			}
		}
		
		return scheme;

	}
	
	public static Color getRGBNegative(Color c){
		return new Color(255-c.getRed(),255-c.getGreen(),255-c.getBlue());
	}

